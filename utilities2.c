#include "cub3d.h"

double
	abs_val(double x)
{
	return (x >= 0 ? x : -x);
}

int
	pos_equals(t_2d pos1, t_2d pos2)
{
	return (pos1.x == pos2.x && pos1.y == pos2.y);
}
